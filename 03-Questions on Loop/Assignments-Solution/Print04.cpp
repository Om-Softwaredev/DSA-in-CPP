#include<iostream>
using namespace std;
int main(){
    // 4.	There is an Arithmetic Progression, 
    // First number is 220 and Common Difference is 7. 
    // So print all the numbers from 220 to 730 which follow the AP.

    for (int i = 220; i <= 730; i=i+7)
    {
        cout<<i<<" ";
    }  
}